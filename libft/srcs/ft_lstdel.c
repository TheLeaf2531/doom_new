#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*ptr;

	if (alst == NULL)
		return ;
	while (*alst)
	{
		ptr = *alst;
		ft_lstdelone(alst, del);
		*alst = ptr->next;
	}
}
