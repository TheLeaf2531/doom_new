#include "libft.h"
#include <stdlib.h>

void	*ft_memdup(const void *mem, size_t n)
{
	void	*memmall;

	if (!(memmall = (void*)malloc(n)))
		return (NULL);
	return (ft_memcpy(memmall, mem, n));
}
