#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	len;
	char	*str;

	if (s1 && s2)
	{
		len = ft_strlen(s1) + ft_strlen(s2);
		if (!(str = (ft_strndup(s1, len))))
			return (NULL);
		return (ft_strcat(str, s2));
	}
	return (NULL);
}
