#include "libft.h"

char	*ft_strrev(char *s)
{
	size_t	start;
	size_t	end;
	char	temp;

	start = 0;
	end = ft_strlen(s) - 1;
	while (start < end)
	{
		temp = s[start];
		s[start] = s[end];
		s[end] = temp;
	}
	return (s);
}
